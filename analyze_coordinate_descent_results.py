#!/usr/bin/env python3
"""Analyze coordinate descent search results and report statistical significance.

This script reads a `coordinate_descent_final.json` file generated by
`hyperparam_search.py`, extracts the best hyperparameter configuration by score,
and computes significance tests comparing it against baseline and runner-up.

Significance tests included:
- Throughput (proofs/second): Poisson rate z-test
- Success rate: two-proportion z-test

The tests are approximate because each configuration is typically evaluated once.
"""

from __future__ import annotations

import argparse
import json
import math
from pathlib import Path
from typing import Any, Dict, List, Tuple


def _normal_cdf(x: float) -> float:
    """Standard normal CDF."""
    return 0.5 * (1.0 + math.erf(x / math.sqrt(2.0)))


def _two_sided_p_from_z(z: float) -> float:
    """Two-sided p-value from a z-statistic."""
    return 2.0 * (1.0 - _normal_cdf(abs(z)))


def _poisson_rate_test(
    k1: int, t1: float, k2: int, t2: float
) -> Tuple[float, float, float]:
    """Compare two Poisson rates via normal approximation.

    Returns (z, p_value, rate_ratio).
    """
    r1 = k1 / t1 if t1 > 0 else 0.0
    r2 = k2 / t2 if t2 > 0 else 0.0
    var = (k1 / (t1 * t1) if t1 > 0 else 0.0) + (k2 / (t2 * t2) if t2 > 0 else 0.0)
    if var <= 0:
        return (
            0.0,
            1.0,
            float("inf") if r2 == 0 and r1 > 0 else (r1 / r2 if r2 else 1.0),
        )
    z = (r1 - r2) / math.sqrt(var)
    p = _two_sided_p_from_z(z)
    ratio = (r1 / r2) if r2 > 0 else float("inf")
    return z, p, ratio


def _two_proportion_test(
    s1: int, n1: int, s2: int, n2: int
) -> Tuple[float, float, float]:
    """Two-proportion z-test.

    Returns (z, p_value, difference) where difference = p1 - p2.
    """
    if n1 <= 0 or n2 <= 0:
        return 0.0, 1.0, 0.0
    p1 = s1 / n1
    p2 = s2 / n2
    pooled = (s1 + s2) / (n1 + n2)
    se = math.sqrt(max(pooled * (1.0 - pooled) * (1.0 / n1 + 1.0 / n2), 0.0))
    if se == 0:
        return 0.0, 1.0, p1 - p2
    z = (p1 - p2) / se
    p = _two_sided_p_from_z(z)
    return z, p, p1 - p2


def _wilson_ci(successes: int, n: int, z: float = 1.96) -> Tuple[float, float]:
    """Wilson interval for a proportion."""
    if n <= 0:
        return 0.0, 0.0
    phat = successes / n
    denom = 1.0 + (z * z) / n
    center = (phat + (z * z) / (2.0 * n)) / denom
    radius = (
        z * math.sqrt((phat * (1.0 - phat) / n) + ((z * z) / (4.0 * n * n))) / denom
    )
    return max(0.0, center - radius), min(1.0, center + radius)


def _poisson_rate_ci(k: int, t: float, z: float = 1.96) -> Tuple[float, float]:
    """Normal-approx confidence interval for a Poisson rate."""
    if t <= 0:
        return 0.0, 0.0
    if k == 0:
        # Rule-of-three upper bound for zero-count Poisson processes.
        return 0.0, 3.0 / t
    rate = k / t
    se = math.sqrt(k) / t
    return max(0.0, rate - z * se), rate + z * se


def _filter_and_sort_trials(trials: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    valid = [t for t in trials if t.get("error_message") in (None, "")]
    return sorted(valid, key=lambda x: float(x.get("score", 0.0)), reverse=True)


def _format_config(config: Dict[str, Any]) -> str:
    excluded = {
        "model_name",
        "data_type",
        "save_training_data",
        "save_checkpoints",
        "use_wandb",
        "train_value_head",
        "use_final_reward",
        "mcts_type",
    }
    keys = [k for k in config.keys() if k not in excluded]
    return "\n".join([f"  - {k}: {config[k]}" for k in keys])


def analyze(path: Path, compare_with: str = "baseline", alpha: float = 0.05) -> str:
    with path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    if not isinstance(data, list) or not data:
        raise ValueError(
            "Input file does not contain a non-empty list of trial results"
        )

    sorted_trials = _filter_and_sort_trials(data)
    if len(sorted_trials) < 2:
        raise ValueError("Need at least two valid trials for comparison")

    best = sorted_trials[0]
    baseline = data[0]
    runner_up = sorted_trials[1]

    if compare_with == "baseline":
        comp = baseline
        comp_name = "baseline"
    elif compare_with == "runner-up":
        comp = runner_up
        comp_name = "runner-up"
    else:
        raise ValueError("compare_with must be 'baseline' or 'runner-up'")

    k_best = int(best["num_proofs_succeeded"])
    t_best = float(best["total_time"])
    n_best = int(best["num_proofs_attempted"])

    k_comp = int(comp["num_proofs_succeeded"])
    t_comp = float(comp["total_time"])
    n_comp = int(comp["num_proofs_attempted"])

    # Throughput significance (Poisson rate).
    z_rate, p_rate, ratio = _poisson_rate_test(k_best, t_best, k_comp, t_comp)
    rate_ci_low, rate_ci_high = _poisson_rate_ci(k_best, t_best)

    # Success-rate significance.
    z_succ, p_succ, succ_diff = _two_proportion_test(k_best, n_best, k_comp, n_comp)
    succ_ci_low, succ_ci_high = _wilson_ci(k_best, n_best)

    sig_rate = p_rate < alpha
    sig_succ = p_succ < alpha

    lines: List[str] = []
    lines.append("Coordinate Descent Result Analysis")
    lines.append("=" * 80)
    lines.append(f"File: {path}")
    lines.append(f"Total trials (raw): {len(data)}")
    lines.append(f"Valid trials (no error): {len(sorted_trials)}")
    lines.append("")

    lines.append("Best Configuration (by score/proofs_per_hour)")
    lines.append("-" * 80)
    lines.append(f"score (proofs/hour): {best['score']:.3f}")
    lines.append(f"proofs/hour: {best['proofs_per_hour']:.3f}")
    lines.append(f"proofs/second: {best['proofs_per_second']:.6f}")
    lines.append(f"success_rate: {best['success_rate']:.3%}")
    lines.append(
        "proof-rate 95% CI (proofs/hour): "
        f"[{rate_ci_low * 3600.0:.3f}, {rate_ci_high * 3600.0:.3f}]"
    )
    lines.append("success-rate 95% CI: " f"[{succ_ci_low:.3%}, {succ_ci_high:.3%}]")
    lines.append("hyperparameters:")
    lines.append(_format_config(best["config"]))
    lines.append("")

    lines.append(f"Significance vs {comp_name}")
    lines.append("-" * 80)
    lines.append(
        f"{comp_name} proofs/hour: {comp['proofs_per_hour']:.3f}, "
        f"success_rate: {comp['success_rate']:.3%}"
    )
    lines.append(
        "Throughput (Poisson rate test): "
        f"z={z_rate:.3f}, p={p_rate:.3g}, rate_ratio={ratio:.3f}, "
        f"significant={sig_rate}"
    )
    lines.append(
        "Success rate (two-proportion z-test): "
        f"z={z_succ:.3f}, p={p_succ:.3g}, delta={succ_diff:.3%}, "
        f"significant={sig_succ}"
    )
    lines.append("")

    lines.append("Top 5 Trials")
    lines.append("-" * 80)
    for i, trial in enumerate(sorted_trials[:5], start=1):
        cfg = trial["config"]
        lines.append(
            f"{i}. proofs/hr={trial['proofs_per_hour']:.3f}, "
            f"success={trial['success_rate']:.3%}, "
            f"workers={cfg.get('num_workers')}, "
            f"batch={cfg.get('batch_size')}, "
            f"tactics={cfg.get('num_tactics_to_expand')}, "
            f"iters={cfg.get('num_iterations')}, "
            f"max_steps={cfg.get('max_steps')}, "
            f"max_rollout_depth={cfg.get('max_rollout_depth')}"
        )

    lines.append("")
    lines.append(
        "Note: These are approximate significance tests from single-run summaries. "
        "For stronger inference, re-evaluate top configs with multiple independent runs."
    )
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(
        description=(
            "Analyze coordinate_descent_final.json, extract best hyperparameters, "
            "and estimate statistical significance."
        )
    )
    parser.add_argument(
        "--input",
        type=Path,
        default=Path("hyperparam_results/coordinate_descent_final.json"),
        help="Path to coordinate descent final results JSON",
    )
    parser.add_argument(
        "--compare-with",
        choices=["baseline", "runner-up"],
        default="baseline",
        help="Comparison target for significance tests",
    )
    parser.add_argument(
        "--alpha",
        type=float,
        default=0.05,
        help="Significance threshold",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=None,
        help="Optional path to save text report",
    )

    args = parser.parse_args()
    report = analyze(args.input, compare_with=args.compare_with, alpha=args.alpha)
    print(report)
    if args.output is not None:
        args.output.write_text(report + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
